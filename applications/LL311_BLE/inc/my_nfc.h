#ifndef _MY_NFC_H_
#define _MY_NFC_H_

#include "nfc_api.h"

/* 默认 NFC 轮询超时时间 (秒) */
#define NFC_DEFAULT_POLL_TIME_S 30

/********************************************************************
**函数名称:  my_nfc_init
**入口参数:  tid      ---        指向线程 ID 变量的指针
**出口参数:  tid      ---        存储启动后的线程 ID
**函数功能:  初始化 NFC 相关的 I2C 设备与电源控制 GPIO 引脚，并启动 NFC 线程
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int my_nfc_init(k_tid_t *tid);

/********************************************************************
**函数名称:  my_nfc_start_poll
**入口参数:  timeout_s ---       轮询超时时间（秒），0 表示使用默认值
**出口参数:  无
**函数功能:  启动 NFC 轮询功能（读到卡或超时后自动进入 HPD）
**返 回 值:  0 表示成功
*********************************************************************/
int my_nfc_start_poll(uint32_t timeout_s);

/********************************************************************
**函数名称:  my_nfc_stop_poll
**入口参数:  无
**出口参数:  无
**函数功能:  停止 NFC 轮询功能并进入 HPD 模式
**返 回 值:  0 表示成功
*********************************************************************/
int my_nfc_stop_poll(void);

/********************************************************************
**函数名称:  my_nfc_pwr_on
**入口参数:  on       ---        true 开启，false 关闭
**出口参数:  无
**函数功能:  控制 NFC 模块的电源使能引脚
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int my_nfc_pwr_on(bool on);

#endif /* _MY_NFC_H_ */
