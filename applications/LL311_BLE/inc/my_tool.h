#ifndef _MY_TOOL_H_
#define _MY_TOOL_H_

/********************************************************************
**函数名称:  my_set_system_time
**入口参数:  _sec ---        Unix 时间戳（从1970年1月1日开始的秒数）
**出口参数:  无
**函数功能:  设置系统时间
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int my_set_system_time(time_t _sec);

/********************************************************************
**函数名称:  my_get_system_time_sec
**入口参数:  无
**出口参数:  无
**函数功能:  获取当前系统时间（Unix 时间戳）
**返 回 值:  返回从1970年1月1日开始的秒数
*********************************************************************/
time_t my_get_system_time_sec(void);

/********************************************************************
**函数名称:  calculate_remaining_seconds
**入口参数:  start_time     ---        开始时间，格式 HHMM（如 "0001"）
**          interval_min   ---        上传间隔，单位：分钟
**          utc_timestamp  ---        当前 UTC 时间戳
**出口参数:  无
**函数功能:  计算距离下一次上报的剩余秒数
**返 回 值:  返回剩余秒数，负值表示已过期
*********************************************************************/
int calculate_remaining_seconds(const char *start_time, int interval_min, long long utc_timestamp);

/********************************************************************
**函数名称:  rand_0_to_120_seconds
**入口参数:  p_seconds      ---        指向存储随机数的指针
**出口参数:  p_seconds      ---        存储生成的随机秒数
**函数功能:  生成 0-120 秒范围内的随机数
**返 回 值:  返回 PSA 状态码
*********************************************************************/
psa_status_t rand_0_to_120_seconds(uint32_t *p_seconds);

#endif