#ifndef _NFC_NTAG_H_
#define _NFC_NTAG_H_

#include <stdint.h>
#include "fm175xx_reg.h"

/* NTag页大小 */
#define NTAG_PAGE_SIZE 4
#define NTAG_READ_SIZE 16 /* 读取时返回16字节（4页） */

/* 外部变量 */
extern uint8_t NTAG_PAGE;
extern uint8_t NTAG_PAGE_DATA[16];

/********************************************************************
**函数名称:  nfc_ntag_read
**入口参数:  page_addr --- 页地址
**出口参数:  data_buf  --- 16字节数据缓冲区
**函数功能:  读取NTag页数据（返回4页共16字节）
**返 回 值:  FM175XX_SUCCESS或错误码
*********************************************************************/
uint8_t nfc_ntag_read(uint8_t page_addr, uint8_t *data_buf);

/********************************************************************
**函数名称:  nfc_ntag_write
**入口参数:  page_addr --- 页地址
**           data_buf  --- 4字节数据
**函数功能:  写入NTag页数据（每页4字节）
**返 回 值:  FM175XX_SUCCESS或错误码
*********************************************************************/
uint8_t nfc_ntag_write(uint8_t page_addr, uint8_t *data_buf);

#endif /* _NFC_NTAG_H_ */
