#ifndef _FM175XX_DRIVER_H_
#define _FM175XX_DRIVER_H_

#include <stdint.h>
#include <stdbool.h>
#include "../../../api/nfc_api.h"

/* FM175XX I2C 设备地址 */
#define FM175XX_I2C_ADDR        0x28

/* 函数声明 */

/********************************************************************
**函数名称:  fm175xx_driver_init
**入口参数:  无
**出口参数:  无
**函数功能:  初始化 FM175XX 驱动，包括 I2C 和 NPD 复位
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_driver_init(void);

/********************************************************************
**函数名称:  fm175xx_npd_reset
**入口参数:  无
**出口参数:  无
**函数功能:  执行 NPD 硬件复位
**返 回 值:  无
*********************************************************************/
void fm175xx_npd_reset(void);

/********************************************************************
**函数名称:  fm175xx_driver_deinit
**入口参数:  无
**出口参数:  无
**函数功能:  反初始化 FM175XX 驱动
**返 回 值:  0 表示成功
*********************************************************************/
int fm175xx_driver_deinit(void);

/********************************************************************
**函数名称:  fm175xx_get_version
**入口参数:  无
**出口参数:  version  ---        版本号存储指针
**函数功能:  读取 FM175XX 芯片版本号
**返 回 值:  0 表示成功
*********************************************************************/
int fm175xx_get_version(uint8_t *version);

/********************************************************************
**函数名称:  fm175xx_poll_start
**入口参数:  cb       ---        卡片检测回调函数
**出口参数:  无
**函数功能:  启动卡片轮询
**返 回 值:  0 表示成功
*********************************************************************/
int fm175xx_poll_start(nfc_card_detected_cb_t cb);

/********************************************************************
**函数名称:  fm175xx_poll_stop
**入口参数:  无
**出口参数:  无
**函数功能:  停止卡片轮询
**返 回 值:  0 表示成功
*********************************************************************/
int fm175xx_poll_stop(void);

/********************************************************************
**函数名称:  fm175xx_read_reg
**入口参数:  reg_addr --- 寄存器地址
**出口参数:  reg_data --- 读取到的数据
**函数功能:  读取 FM175XX 寄存器
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_read_reg(uint8_t reg_addr, uint8_t *reg_data);

/********************************************************************
**函数名称:  fm175xx_write_reg
**入口参数:  reg_addr --- 寄存器地址
**           reg_data --- 要写入的数据
**函数功能:  写入 FM175XX 寄存器
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_write_reg(uint8_t reg_addr, uint8_t reg_data);

/********************************************************************
**函数名称:  fm175xx_modify_reg
**入口参数:  reg_addr --- 寄存器地址
**           mask     --- 要修改的位掩码
**           set      --- 要设置的值
**函数功能:  修改 FM175XX 寄存器的特定位
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_modify_reg(uint8_t reg_addr, uint8_t mask, uint8_t set);

/********************************************************************
**函数名称:  fm175xx_read_fifo
**入口参数:  length   --- 要读取的字节数
**出口参数:  data_buf --- 数据缓冲区
**函数功能:  从 FIFO 读取数据
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_read_fifo(uint8_t length, uint8_t *data_buf);

/********************************************************************
**函数名称:  fm175xx_write_fifo
**入口参数:  length   --- 要写入的字节数
**           data_buf --- 数据缓冲区
**函数功能:  向 FIFO 写入数据
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_write_fifo(uint8_t length, uint8_t *data_buf);

/********************************************************************
**函数名称:  fm175xx_clear_fifo
**入口参数:  无
**函数功能:  清空 FIFO
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_clear_fifo(void);

/********************************************************************
**函数名称:  fm175xx_check_fifo
**入口参数:  无
**函数功能:  验证 FIFO 读写功能是否正常
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_check_fifo(void);

/********************************************************************
**函数名称:  fm175xx_enter_hpd
**入口参数:  无
**出口参数:  无
**函数功能:  进入 Hard Power Down (HPD) 模式，NPD 拉低
**返 回 值:  0 表示成功
*********************************************************************/
int fm175xx_enter_hpd(void);

/********************************************************************
**函数名称:  fm175xx_exit_hpd
**入口参数:  无
**出口参数:  无
**函数功能:  退出 HPD 模式，重新初始化芯片
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_exit_hpd(void);

/********************************************************************
**函数名称:  fm175xx_read_reg
**入口参数:  reg      ---        寄存器地址
**           data     ---        数据缓冲区
**出口参数:  data     ---        读取到的数据
**函数功能:  读取 FM175XX 寄存器
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int fm175xx_read_reg(uint8_t reg, uint8_t *data);

#endif /* _FM175XX_DRIVER_H_ */
